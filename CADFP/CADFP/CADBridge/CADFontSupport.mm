@implementation CADFontSupport

static NSString *_cachedFontsDirectoryPath = nil;

+ (NSString *)fontsDirectoryPath
{
    return _cachedFontsDirectoryPath;
}

+ (void)setupIfNeeded
{
    static dispatch_once_t onceToken;
    dispatch_once(&onceToken, ^{
        NSString *path = [self prepareFontsDirectory];
        _cachedFontsDirectoryPath = [path copy];

        if (path.length == 0) {
            NSLog(@"[CADFontSupport] ❌ Failed to prepare fonts directory.");
            return;
        }

        [self installEnvironmentVariables:path];
        [self writeFontMapAtPath:path];

        // List every file in the cache so we can see what ODA will find.
        NSArray<NSString *> *cached = [NSFileManager.defaultManager
            contentsOfDirectoryAtPath:path error:nil];
        NSLog(@"[CADFontSupport] Font search path: %@", path);
        NSLog(@"[CADFontSupport] Files in cache dir: %@",
              cached.count ? [cached componentsJoinedByString:@", "] : @"(empty)");
    });
}

#pragma mark - Internals

+ (NSString *)prepareFontsDirectory
{
    NSFileManager *fm = NSFileManager.defaultManager;

    NSArray<NSString *> *caches = NSSearchPathForDirectoriesInDomains(
        NSCachesDirectory, NSUserDomainMask, YES);
    if (caches.count == 0) return nil;

    NSString *dst = [caches.firstObject
        stringByAppendingPathComponent:kCADFontsCacheSubdirectory];

    NSError *err = nil;
    if (![fm fileExistsAtPath:dst]) {
        if (![fm createDirectoryAtPath:dst
           withIntermediateDirectories:YES attributes:nil error:&err]) {
            NSLog(@"[CADFontSupport] createDirectory failed: %@", err);
            return nil;
        }
    }

    // Locate the bundle's Fonts/ folder reference.
    NSString *bundleFonts = [NSBundle.mainBundle.resourcePath
        stringByAppendingPathComponent:kCADFontsBundleSubdirectory];

    BOOL isDir = NO;
    if ([fm fileExistsAtPath:bundleFonts isDirectory:&isDir] && isDir) {
        NSArray<NSString *> *entries = [fm contentsOfDirectoryAtPath:bundleFonts error:nil];
        NSLog(@"[CADFontSupport] Bundle Fonts/ contents: %@",
              entries.count ? [entries componentsJoinedByString:@", "] : @"(empty)");

        for (NSString *name in entries) {
            if ([name hasPrefix:@"."]) continue;

            NSString *srcPath = [bundleFonts stringByAppendingPathComponent:name];
            NSString *dstPath = [dst stringByAppendingPathComponent:name];

            // README is informational only — skip it.
            if ([name.lowercaseString hasSuffix:@".md"]) continue;

            // Always overwrite FONTMAP.txt so code changes propagate.
            BOOL isFontMap = [name.lowercaseString
                isEqualToString:kCADFontMapFileName.lowercaseString];
            if ([fm fileExistsAtPath:dstPath]) {
                if (!isFontMap) {
                    NSLog(@"[CADFontSupport] Already cached: %@", name);
                    continue;
                }
                [fm removeItemAtPath:dstPath error:nil];
            }

            NSError *copyErr = nil;
            if ([fm copyItemAtPath:srcPath toPath:dstPath error:&copyErr]) {
                NSLog(@"[CADFontSupport] Copied to cache: %@", name);
            } else {
                NSLog(@"[CADFontSupport] Copy FAILED %@ → %@: %@",
                      srcPath, dstPath, copyErr);
            }
        }
    } else {
        NSLog(@"[CADFontSupport] ⚠️ Bundle Fonts/ directory not found at: %@", bundleFonts);
        NSLog(@"[CADFontSupport] ⚠️ Make sure Fonts is added as a folder reference (blue) in Copy Bundle Resources.");
    }

    return dst;
}

+ (void)writeFontMapAtPath:(NSString *)directory
{
    NSString *mapPath = [directory
        stringByAppendingPathComponent:kCADFontMapFileName];

    // Check whether the bundle ships a hand-crafted FONTMAP.txt.
    // If so, the copy step above already placed it in the cache — use it.
    NSString *bundleMap = [[NSBundle.mainBundle.resourcePath
        stringByAppendingPathComponent:kCADFontsBundleSubdirectory]
        stringByAppendingPathComponent:kCADFontMapFileName];

    if (![NSFileManager.defaultManager fileExistsAtPath:bundleMap]) {
        // No bundle-supplied map: write the generated default every launch
        // so tweaking the in-code table is all that's needed.
        NSError *writeErr = nil;
        if (![CADDefaultFontMap() writeToFile:mapPath
                                   atomically:YES
                                     encoding:NSUTF8StringEncoding
                                        error:&writeErr]) {
            NSLog(@"[CADFontSupport] write FONTMAP.txt failed: %@", writeErr);
            return;
        }
        NSLog(@"[CADFontSupport] FONTMAP.txt written.");
    }

    // FONTMAP env var: some ODA host services read it via getenv("FONTMAP").
    setenv("FONTMAP", mapPath.fileSystemRepresentation, 1);
}

+ (void)installEnvironmentVariables:(NSString *)directory
{
    const char *cPath = directory.fileSystemRepresentation;
    if (!cPath) return;

    // ODA's OdDbHostAppServices::findFile() searches:
    //   • ACAD     — general support-file search path (semicolon-separated)
    //   • ACADFONTS— dedicated font search path (checked first for font files)
    setenv("ACAD",           cPath, 1);
    setenv("ACADFONTS",      cPath, 1);
    setenv("ODA_DRAWINGS_PATH", cPath, 1);

    NSLog(@"[CADFontSupport] ACAD=%s", cPath);
}

@end
