#import "CADFontSupport.h"

#import <stdlib.h>

static NSString *kCADFontsCacheSubdirectory  = @"CADFonts";
static NSString *kCADFontsBundleSubdirectory = @"Fonts";
static NSString *kCADFontMapFileName         = @"FONTMAP.txt";

// Bundled font filenames — must match what is inside CADFP/Fonts/.
static NSString *const kCADShxPrimary  = @"hztxt.shx";   // primary CJK SHX bigfont
static NSString *const kCADShxSecondary= @"gbcbig.shx";  // secondary CJK SHX bigfont
static NSString *const kCADTtcFallback = @"NotoSansCJK-wght-400-900.ttf.ttc";

// ─────────────────────────────────────────────────────────────────────────────
// CRITICAL RULE FOR ODA FONTMAP:
//   • SHX bigfont names  →  must resolve to another .shx file.
//     Mapping an SHX name to a .ttf/.ttc is INVALID — ODA will try to use the
//     TTF as an SHX bigfont, fail, and Chinese will still show as "?".
//   • TTF family names   →  may resolve to a .ttf/.ttc (handled by FreeType).
//
// Therefore:
//   - hztxt and gbcbig are NOT listed here; findFile() will find them directly
//     via the ACAD search path.
//   - Unknown SHX names map to hztxt.shx (SHX → SHX substitution is valid).
//   - Windows CJK TTF family names map to the Noto TTC (TTF → TTF is valid).
// ─────────────────────────────────────────────────────────────────────────────
static NSString *CADDefaultFontMap(void)
{
    NSString *shxP = kCADShxPrimary;
    NSString *shxS = kCADShxSecondary;
    NSString *ttc  = kCADTtcFallback;

    NSMutableString *buf = [NSMutableString string];
    [buf appendString:@"# CADFP FONTMAP — auto-generated by CADFontSupport\n"];
    [buf appendString:@"# SHX->SHX and TTF->TTF only. Never map SHX to TTF.\n#\n"];

    // SHX bigfont names we do NOT have bundled → redirect to hztxt.shx
    NSArray<NSArray<NSString *> *> *shxRows = @[
        @[@"chinese",      shxP],
        @[@"chinese.shx",  shxP],
        @[@"hzst",         shxP],
        @[@"hzfs",         shxP],
        @[@"hzdx",         shxP],
        @[@"hzdx.shx",     shxP],
        @[@"hzxt",         shxP],
        @[@"hz",           shxP],
        @[@"tssdeng",      shxP],
        @[@"tssdeng.shx",  shxP],
        @[@"tssdchn",      shxP],
        @[@"tssdchn.shx",  shxP],
        @[@"gbenor",       shxS],   // regular SHX often paired with gbcbig
        @[@"gbenor.shx",   shxS],
        @[@"gbeitc",       shxS],
        @[@"gbeitc.shx",   shxS],
    ];

    // Windows CJK TTF family names → Noto Sans CJK TTC (TTF pipeline)
    NSArray<NSArray<NSString *> *> *ttfRows = @[
        @[@"宋体",            ttc],
        @[@"SimSun",          ttc],
        @[@"新宋体",          ttc],
        @[@"NSimSun",         ttc],
        @[@"仿宋",            ttc],
        @[@"仿宋_GB2312",     ttc],
        @[@"FangSong",        ttc],
        @[@"FangSong_GB2312", ttc],
        @[@"黑体",            ttc],
        @[@"SimHei",          ttc],
        @[@"楷体",            ttc],
        @[@"楷体_GB2312",     ttc],
        @[@"KaiTi",           ttc],
        @[@"KaiTi_GB2312",    ttc],
        @[@"微软雅黑",        ttc],
        @[@"Microsoft YaHei", ttc],
        @[@"华文细黑",        ttc],
        @[@"STHeiti",         ttc],
        @[@"华文黑体",        ttc],
        @[@"等线",            ttc],
        @[@"DengXian",        ttc],
    ];

    [buf appendString:@"# SHX bigfont substitutions (SHX → SHX):\n"];
    for (NSArray<NSString *> *row in shxRows) {
        [buf appendFormat:@"%@;%@\n", row[0], row[1]];
    }
    [buf appendString:@"# TTF family substitutions (TTF → TTF):\n"];
    for (NSArray<NSString *> *row in ttfRows) {
        [buf appendFormat:@"%@;%@\n", row[0], row[1]];
    }
    return buf;
}

